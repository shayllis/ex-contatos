class Contact {
    constructor (id, name, phone, imageURI, lat, lng, created_at) {
        this.id = id;
        this.name = name;
        this.phone = phone;
        this.imageURI = imageURI;
        this.lat = lat;
        this.lng = lng;
        this.created_at = created_at;
    }
}

export default Contact;